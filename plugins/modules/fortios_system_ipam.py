def filter_system_ipam_data(json):
    option_list = [
        "automatic_conflict_resolution",
        "manage_lan_addresses",
        "manage_lan_extension_addresses",
        "manage_ssid_addresses",
        "pool_subnet",
        "pools",
        "rules",
        "server_type",
        "status",
    ]

    json = remove_invalid_fields(json)
    dictionary = {}

    for attribute in option_list:
        if attribute in json and json[attribute] is not None:
            dictionary[attribute] = json[attribute]

    return dictionary


def underscore_to_hyphen(data):
    if isinstance(data, list):
        for i, elem in enumerate(data):
            data[i] = underscore_to_hyphen(elem)
    elif isinstance(data, dict):
        new_data = {}
        for k, v in data.items():
            new_data[k.replace("_", "-")] = underscore_to_hyphen(v)
        data = new_data

    return data


def system_ipam(data, fos):
    vdom = data["vdom"]
    system_ipam_data = data["system_ipam"]
    filtered_data = underscore_to_hyphen(filter_system_ipam_data(system_ipam_data))

    return fos.set("system", "ipam", data=filtered_data, vdom=vdom)


def is_successful_status(resp):
    return (
        "status" in resp
        and resp["status"] == "success"
        or "http_status" in resp
        and resp["http_status"] == 200
        or "http_method" in resp
        and resp["http_method"] == "DELETE"
        and resp["http_status"] == 404
    )


def fortios_system(data, fos):

    fos.do_member_operation("system", "ipam")
    if data["system_ipam"]:
        resp = system_ipam(data, fos)
    else:
        fos._module.fail_json(msg="missing task body: %s" % ("system_ipam"))

    return (
        not is_successful_status(resp),
        is_successful_status(resp)
        and (resp["revision_changed"] if "revision_changed" in resp else True),
        resp,
        {},
    )


