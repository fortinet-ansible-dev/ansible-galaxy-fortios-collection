def filter_authentication_setting_data(json):
    option_list = [
        "active_auth_scheme",
        "auth_https",
        "captive_portal",
        "captive_portal_ip",
        "captive_portal_ip6",
        "captive_portal_port",
        "captive_portal_ssl_port",
        "captive_portal_type",
        "captive_portal6",
        "cert_auth",
        "cert_captive_portal",
        "cert_captive_portal_ip",
        "cert_captive_portal_port",
        "cookie_max_age",
        "cookie_refresh_div",
        "dev_range",
        "ip_auth_cookie",
        "persistent_cookie",
        "sso_auth_scheme",
        "update_time",
        "user_cert_ca",
    ]

    json = remove_invalid_fields(json)
    dictionary = {}

    for attribute in option_list:
        if attribute in json and json[attribute] is not None:
            dictionary[attribute] = json[attribute]

    return dictionary


def underscore_to_hyphen(data):
    if isinstance(data, list):
        for i, elem in enumerate(data):
            data[i] = underscore_to_hyphen(elem)
    elif isinstance(data, dict):
        new_data = {}
        for k, v in data.items():
            new_data[k.replace("_", "-")] = underscore_to_hyphen(v)
        data = new_data

    return data


def authentication_setting(data, fos):
    vdom = data["vdom"]
    authentication_setting_data = data["authentication_setting"]
    filtered_data = underscore_to_hyphen(
        filter_authentication_setting_data(authentication_setting_data)
    )

    return fos.set("authentication", "setting", data=filtered_data, vdom=vdom)


def is_successful_status(resp):
    return (
        "status" in resp
        and resp["status"] == "success"
        or "http_status" in resp
        and resp["http_status"] == 200
        or "http_method" in resp
        and resp["http_method"] == "DELETE"
        and resp["http_status"] == 404
    )


def fortios_authentication(data, fos):

    fos.do_member_operation("authentication", "setting")
    if data["authentication_setting"]:
        resp = authentication_setting(data, fos)
    else:
        fos._module.fail_json(msg="missing task body: %s" % ("authentication_setting"))

    return (
        not is_successful_status(resp),
        is_successful_status(resp)
        and (resp["revision_changed"] if "revision_changed" in resp else True),
        resp,
        {},
    )


