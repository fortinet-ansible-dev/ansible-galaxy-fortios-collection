def filter_log_fortianalyzer_cloud_override_filter_data(json):
    option_list = [
        "anomaly",
        "dlp_archive",
        "dns",
        "filter",
        "filter_type",
        "forward_traffic",
        "free_style",
        "gtp",
        "local_traffic",
        "multicast_traffic",
        "netscan_discovery",
        "netscan_vulnerability",
        "severity",
        "sniffer_traffic",
        "ssh",
        "voip",
        "ztna_traffic",
    ]

    json = remove_invalid_fields(json)
    dictionary = {}

    for attribute in option_list:
        if attribute in json and json[attribute] is not None:
            dictionary[attribute] = json[attribute]

    return dictionary


def underscore_to_hyphen(data):
    if isinstance(data, list):
        for i, elem in enumerate(data):
            data[i] = underscore_to_hyphen(elem)
    elif isinstance(data, dict):
        new_data = {}
        for k, v in data.items():
            new_data[k.replace("_", "-")] = underscore_to_hyphen(v)
        data = new_data

    return data


def log_fortianalyzer_cloud_override_filter(data, fos):
    vdom = data["vdom"]
    log_fortianalyzer_cloud_override_filter_data = data[
        "log_fortianalyzer_cloud_override_filter"
    ]
    filtered_data = underscore_to_hyphen(
        filter_log_fortianalyzer_cloud_override_filter_data(
            log_fortianalyzer_cloud_override_filter_data
        )
    )

    return fos.set(
        "log.fortianalyzer-cloud", "override-filter", data=filtered_data, vdom=vdom
    )


def is_successful_status(resp):
    return (
        "status" in resp
        and resp["status"] == "success"
        or "http_status" in resp
        and resp["http_status"] == 200
        or "http_method" in resp
        and resp["http_method"] == "DELETE"
        and resp["http_status"] == 404
    )


def fortios_log_fortianalyzer_cloud(data, fos):

    fos.do_member_operation("log.fortianalyzer-cloud", "override-filter")
    if data["log_fortianalyzer_cloud_override_filter"]:
        resp = log_fortianalyzer_cloud_override_filter(data, fos)
    else:
        fos._module.fail_json(
            msg="missing task body: %s" % ("log_fortianalyzer_cloud_override_filter")
        )

    return (
        not is_successful_status(resp),
        is_successful_status(resp)
        and (resp["revision_changed"] if "revision_changed" in resp else True),
        resp,
        {},
    )


