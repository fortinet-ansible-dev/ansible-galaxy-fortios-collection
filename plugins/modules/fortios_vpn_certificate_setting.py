def filter_vpn_certificate_setting_data(json):
    option_list = ['certname_dsa1024', 'certname_dsa2048', 'certname_ecdsa256',
                   'certname_ecdsa384', 'certname_ecdsa521', 'certname_ed25519',
                   'certname_ed448', 'certname_rsa1024', 'certname_rsa2048',
                   'certname_rsa4096', 'check_ca_cert', 'check_ca_chain',
                   'cmp_key_usage_checking', 'cmp_save_extra_certs', 'cn_match',
                   'interface', 'interface_select_method', 'ocsp_default_server',
                   'ocsp_option', 'ocsp_status', 'ssl_min_proto_version',
                   'ssl_ocsp_option', 'ssl_ocsp_source_ip', 'ssl_ocsp_status',
                   'strict_crl_check', 'strict_ocsp_check', 'subject_match']
    dictionary = {}

    for attribute in option_list:
        if attribute in json and json[attribute] is not None:
            dictionary[attribute] = json[attribute]

    return dictionary


def underscore_to_hyphen(data):
    if isinstance(data, list):
        for i, elem in enumerate(data):
            data[i] = underscore_to_hyphen(elem)
    elif isinstance(data, dict):
        new_data = {}
        for k, v in data.items():
            new_data[k.replace('_', '-')] = underscore_to_hyphen(v)
        data = new_data

    return data


def vpn_certificate_setting(data, fos):
    vdom = data['vdom']
    vpn_certificate_setting_data = data['vpn_certificate_setting']
    filtered_data = underscore_to_hyphen(filter_vpn_certificate_setting_data(vpn_certificate_setting_data))

    return fos.set('vpn.certificate',
                   'setting',
                   data=filtered_data,
                   vdom=vdom)


def is_successful_status(status):
    return status['status'] == "success" or \
        status['http_method'] == "DELETE" and status['http_status'] == 404


def fortios_vpn_certificate(data, fos):

    if data['vpn_certificate_setting']:
        resp = vpn_certificate_setting(data, fos)
    else:
        fos._module.fail_json(msg='missing task body: %s' % ('vpn_certificate_setting'))

    return not is_successful_status(resp), \
        resp['status'] == "success" and \
        (resp['revision_changed'] if 'revision_changed' in resp else True), \
        resp


