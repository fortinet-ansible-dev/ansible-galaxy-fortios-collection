def filter_vpn_ocvpn_data(json):
    option_list = [
        "auto_discovery",
        "auto_discovery_shortcut_mode",
        "eap",
        "eap_users",
        "forticlient_access",
        "ha_alias",
        "ip_allocation_block",
        "multipath",
        "nat",
        "overlays",
        "poll_interval",
        "role",
        "sdwan",
        "sdwan_zone",
        "status",
        "subnets",
        "wan_interface",
    ]

    json = remove_invalid_fields(json)
    dictionary = {}

    for attribute in option_list:
        if attribute in json and json[attribute] is not None:
            dictionary[attribute] = json[attribute]

    return dictionary


def underscore_to_hyphen(data):
    if isinstance(data, list):
        for i, elem in enumerate(data):
            data[i] = underscore_to_hyphen(elem)
    elif isinstance(data, dict):
        new_data = {}
        for k, v in data.items():
            new_data[k.replace("_", "-")] = underscore_to_hyphen(v)
        data = new_data

    return data


def vpn_ocvpn(data, fos):
    vdom = data["vdom"]
    vpn_ocvpn_data = data["vpn_ocvpn"]
    filtered_data = underscore_to_hyphen(filter_vpn_ocvpn_data(vpn_ocvpn_data))

    return fos.set("vpn", "ocvpn", data=filtered_data, vdom=vdom)


def is_successful_status(resp):
    return (
        "status" in resp
        and resp["status"] == "success"
        or "http_status" in resp
        and resp["http_status"] == 200
        or "http_method" in resp
        and resp["http_method"] == "DELETE"
        and resp["http_status"] == 404
    )


def fortios_vpn(data, fos):

    fos.do_member_operation("vpn", "ocvpn")
    if data["vpn_ocvpn"]:
        resp = vpn_ocvpn(data, fos)
    else:
        fos._module.fail_json(msg="missing task body: %s" % ("vpn_ocvpn"))

    return (
        not is_successful_status(resp),
        is_successful_status(resp)
        and (resp["revision_changed"] if "revision_changed" in resp else True),
        resp,
        {},
    )


